window.translations = translations;

function applyTranslations(lang) {
  const selected = translations[lang] || translations.es;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach(function(element) {
    const key = element.getAttribute("data-i18n");
    if (selected[key]) element.textContent = selected[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(function(element) {
    const key = element.getAttribute("data-i18n-placeholder");
    if (selected[key]) element.setAttribute("placeholder", selected[key]);
  });

  document.querySelectorAll("[data-i18n-value]").forEach(function(element) {
    const key = element.getAttribute("data-i18n-value");
    if (selected[key]) element.value = selected[key];
  });

  document.querySelectorAll(".lang-btn").forEach(function(btn) {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  localStorage.setItem("tgr-language", lang);
}

window.applyTranslations = applyTranslations;

document.addEventListener("DOMContentLoaded", function() {
  const savedLanguage = localStorage.getItem("tgr-language") || "es";
  applyTranslations(savedLanguage);

  document.querySelectorAll(".lang-btn").forEach(function(btn) {
    btn.addEventListener("click", function() {
      applyTranslations(btn.dataset.lang);
    });
  });
});
